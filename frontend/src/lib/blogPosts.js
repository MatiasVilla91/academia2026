export function getPost(slug) {
  return blogPosts.find((p) => p.slug === slug) ?? null;
}

export function getRelatedPosts(post, limit = 3) {
  return blogPosts
    .filter((item) => item.slug !== post.slug && item.category === post.category)
    .slice(0, limit);
}
